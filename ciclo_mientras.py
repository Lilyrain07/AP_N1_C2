#   ciclo WHILE (Mientras)
#un ciclo es un BUCLE que se repite a base de una condición logica
# cada repeticion se llama ITERACION

#un ciclo while permite realizar una serie de acciones posteriores a la validacion de la condicion logica

contador = 0

while contador <= 10 :
    print(f'valor del contador: {contador}')
    #contador = contador +1
    contador += 1
